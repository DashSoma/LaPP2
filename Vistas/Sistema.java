    private void btnInsertarClienteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnInsertarClienteActionPerformed
        // TODO add your handling code here:
        try {
            cliente = new Cliente(txtCedulaC.getText(), txtNombreC.getText(),
                    txtDireccionC.getText(), txtTelefonoC.getText(), txtCorreoC.getText());
            clienteController.create(cliente);
            clienteController.readAll();
            btnLimpiarActionPerformed(null);
        } catch (Exception e) {
            showError("Error al agregar cliente: " + e.getMessage());
        }
    }//GEN-LAST:event_btnInsertarClienteActionPerformed

