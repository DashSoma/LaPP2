    /**
     * Botón para eliminar un registro seleccionado.
     *
     * @param evt
     */
    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        if (tblPlusesColab.getSelectedRowCount() == 1) {
            int resp = JOptionPane.showConfirmDialog(this, "¿Desea borrar el resgitro?");
            if (resp == 0) {  //El usuario quiere eliminar, Respuesta si
                int fila = tblPlusesColab.getSelectedRow();
                if (listaPColab.remove(listaPColab.get(fila))) {
                    mostrarTabla();
                } else {
                    JOptionPane.showMessageDialog(this, "Error de borrado");
                }
            }
        } else {
            JOptionPane.showMessageDialog(this, "Se debe seleccionar 1 registro");
        }
    }//GEN-LAST:event_btnEliminarActionPerformed
    /**
     * Botón para editar un regsitro seleccionado.
     *
     * @param evt
     */
    private void btnEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarActionPerformed
        if (tblPlusesColab.getSelectedRowCount() == 1) {

            int fila = tblPlusesColab.getSelectedRow();
            pluses = new PlusesColab();

            if (cmbNombrePlus.getSelectedItem() != null) {

                pluses.setCedula(Integer.parseInt(
                tblPlusesColab.getValueAt(fila, 0).toString()));
                pluses.setNombresPluses(cmbNombrePlus.getSelectedItem().toString()); 

                if (listaPColab.set(fila, pluses) != null) {
                    JOptionPane.showMessageDialog(
                            this, "Plus editado");
                    mostrarTabla();
                    btnLimpiarActionPerformed(null);
                } else {
                    JOptionPane.showMessageDialog(
                            this, "Error al editar");
                }
            } else {
                JOptionPane.showMessageDialog(this, "Debe seleccionar un nombre de plus");
            }
        } else {
            JOptionPane.showMessageDialog(this, "Se debe seleccionar 1 registro para editar");
        }
    }//GEN-LAST:event_btnEditarActionPerformed
