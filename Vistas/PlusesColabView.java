    /**
     * Botón para insertar todos los datos al JTable de PlusesColaborador.
     *
     * @param evt ActionPerformed
     */
    private void btnInsertarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnInsertarActionPerformed
        PlusesColab plusesColab = new PlusesColab();

        try {
            // Obtener y limpiar el texto del campo de cédula
            String textValue = txtCedulaT.getText().trim();

            // Verificar si el campo de cédula no está vacío
            if (!textValue.isEmpty()) {
                int cedula = Integer.parseInt(textValue); // Convertir a entero

                // Verificar si se seleccionó un ítem en el combo box
                if (cmbNombrePlus.getSelectedIndex() != -1) {
                    String nombrePlus = cmbNombrePlus.getItemAt(cmbNombrePlus.getSelectedIndex());

                    // Verificar si ya existe el nombre y la cédula en la base de datos
                    if (existeNombre(nombrePlus) && existeCedula(cedula)) {
                        JOptionPane.showMessageDialog(this, "Este Plus ya fue asignado al colaborador seleccionado.");
                    } else {
                        // Asignar los valores al objeto plusesColab
                        plusesColab.setCedula(cedula);
                        plusesColab.setNombresPluses(nombrePlus);

                        // Intentar agregar el plus al colaborador
                        if (listaPColab.add(plusesColab)) {
                            JOptionPane.showMessageDialog(this, "Plus asignado");
                            mostrarTabla();
                            btnLimpiarActionPerformed(null); // Limpiar el formulario
                        } else {
                            JOptionPane.showMessageDialog(this, "Error al insertar");
                        }
                    }
                } else {
                    // Mostrar un mensaje si no se seleccionó ningún ítem en el combo box
                    JOptionPane.showMessageDialog(this, "Debe seleccionar un plus del combo box.", "Selección inválida", JOptionPane.WARNING_MESSAGE);
                }
            } else {
                // Mostrar un mensaje si el campo de cédula está vacío
                JOptionPane.showMessageDialog(this, "El campo de cédula no puede estar vacío. Por favor, ingrese un valor.");
            }

        } catch (NumberFormatException ex) {
            // Manejar la excepción si el valor de la cédula no es un número válido
            JOptionPane.showMessageDialog(this, "El valor ingresado en la cédula no es un número válido. Por favor, verifique e intente nuevamente.", "Error de formato", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnInsertarActionPerformed
    /**
     * Evento en el cual busca los registros que hay en el JTable.
     *
     * @param evt
     */

    private void txtBuscarKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtBuscarKeyReleased
        String titulos[] = {"CédulaTrabajador", "PlusesAgregados"};
        model = new DefaultTableModel(null, titulos);

        for (int i = 0; i < listaPColab.size(); i++) {
            pluses = listaPColab.get(i);
            if (String.valueOf(pluses.getCedula()).toLowerCase().contains(txtCedulaT.getText().toLowerCase())) {
                Object nuevaFila[] = {pluses.getCedula(), pluses.getNombresPluses()};
                model.addRow(nuevaFila);
            }
        }
        tblPlusesColab.setModel(model);

        txtCant.setText(String.valueOf(model.getRowCount()));
    }//GEN-LAST:event_txtBuscarKeyReleased

