    /**
     * Muestra los datos y títulos en el JTable
     */
    public void mostrarTabla() {
        String titulos[] = {"CédulaTrabajador", "PlusesAgregados"};
        model = new DefaultTableModel(null, titulos);
        //No se usó el foreach porque el índice lo estamos necesitando 
        for (int i = 0; i < listaPColab.size(); i++) {
            pluses = listaPColab.get(i);
            Object nuevaFila[] = {pluses.getCedula(), pluses.getNombresPluses()};
            model.addRow(nuevaFila);
        }
        tblPlusesColab.setModel(model);

        txtCant.setText(String.valueOf(model.getRowCount()));
    }

    /**
     * Verifica si un plus con el nombre dado ya existe en la lista de
     * PlusesColab.
     *
     * @param nombresPluses El nombre del plus a verificar.
     * @return true si el nombre del plus ya existe en la lista, false en caso
     * contrario.
     */
    public boolean existeNombre(String nombresPluses) {
        for (int i = 0; i < listaPColab.size(); i++) {
            pluses = listaPColab.get(i);
            if (pluses.getNombresPluses().equals(nombresPluses)) {
                return true;
            }
        }
        return false;
    }

    /**
     * Verifica si un empleado con la cédula dada ya existe en la lista de
     * PlusesColab.
     *
     * @param cedulaEmpleado La cédula del empleado a verificar.
     * @return true si la cédula del empleado ya existe en la lista, false en
     * caso contrario.
     */
    public boolean existeCedula(int cedulaEmpleado) {
        for (int i = 0; i < listaPColab.size(); i++) {
            pluses = listaPColab.get(i);
            if (pluses.getCedula() == cedulaEmpleado) {
                return true;
            }
        }
        return false;
    }

